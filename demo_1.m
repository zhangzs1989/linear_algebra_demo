% 下三角形方程组
a=[5 0 0 0;1 3 0 0;3 4 6 0;-1 3 -6 -1];
b=[-10;4;2;5];
x = zeros(4,1);
x(1) = b(1)/a(1,1);
for i = 2:4
   j = (1:i-1)';
   x(i) = (b(i)-a(i,j)*x(j))/a(i,i);
   disp(x)
end

